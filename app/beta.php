<p>Many people have type 2 diabetes and don’t realise. This is because symptoms
don’t necessarily make you feel unwell.</p>
<p>Symptoms of type 2 diabetes include:</p>
<ul>
<li>peeing more than usual, particularly at night</li>
<li>feeling thirsty all the time</li>
<li>feeling very tired</li>
<li>losing weight without trying</li>
<li>itching around your penis or vagina, or you keep getting thrush</li>
<li>cuts or wounds that take longer to heal</li>
<li>blurred vision</li>
</ul>
<p>You’re more at risk of developing type 2 diabetes if you:</p>
<ul>
<li>are over 40 (or 25 for South Asian people)</li>
<li>have a close relative with diabetes (parent, brother or sister)</li>
<li>are overweight or obese</li>
<li>are of South Asian, Chinese, African-Caribbean or black African origin
(even if you were born in the UK)</li>
</ul>
<h3 id="when-to-see-your-gp">When to see your GP</h3>
<p>See your GP if you have any of the symptoms of type 2 diabetes, or you’re
worried you may have a higher risk of developing it.</p>
<p><a href="getting-diagnosed">Your GP can diagnose diabetes</a>. You’ll need a blood test
which you may have to go to your local health centre for if it can’t be done at
your GP surgery.</p>
<p>The earlier diabetes is diagnosed and treatment starts, the better. Treating
diabetes early reduces your risk of <a href="health-problems">other health problems</a>.</p>

  
